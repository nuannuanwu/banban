                <p class="title">什么是班费卡？</p> 
                
                <p>班费卡是班费的一种承载形式，每位班班用户都可以赚取班费卡，并且可以将班费卡捐赠给指定的班级。用户个人班费卡不可转赠其他用户。</p> 
                
                <p class="title">班费及班费卡的获取途径</p>

                <p>1. 通过参与班班手机应用里的“挣班费”活动，可给班级挣班费：对于调查问卷类的广告，每个调查问卷首次填写完成奖励0.5元班费，之后再次查看或填写该调查问卷不再奖励班费。对于浏览类的广告，每天首次点击浏览，随机奖励0.1元或0.2元班费，每天只奖励一次。</p>
                <p>2. 通过注册班班账号，或用第三方（QQ、微信）登录后首次绑定手机号码，即可获得0.5~10元范围内随机金额的班费卡一张。</p>
                <p>3. 邀请他人成功注册成为班班用户，邀请人将可获得0.5~10元范围内随机金额的班费卡一张。若受邀注册用户同时还通过了教师认证，则邀请人还可再获得一张10元班费卡奖励。</p>
                <p>4. 参与班班平台举行的活动可赚取班费或班费卡，具体赚取方式和金额，以活动公布为准。</p>

               <p class="title">班费卡及班费的使用</p>

                <p>1. 用户所拥有的班费卡可自由捐赠给自己所加入的班级。</p>
                <p>2. 所在班级的班费可以由通过教师认证的班主任在班班网页端进行转出提现，或发“紧急通知”时使用。班费转出申请提交之后，需要班班审核通过方能转出，审核结果会在3个工作日内以手机短信的方式反馈给您。</p>
                <p>3. 班班平台每日可转出名额为1000人。</p>
                <p>4. 每个班主任（不区分班级）每月只能转出一次班费，当班费余额大于等于200元才可转出，且转出金额最高为800元。</p>
                <p>5. 认证老师的个人班费卡可兑换成青豆，在手机客户端的“青豆派兑”页面购买商品。</p>

